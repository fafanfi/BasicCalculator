    /**
     * Tampilkan hasil.
     * @param jlabel 
     */
    private void printResult(JLabel jlabel){
        double tempt = lastResult;
        String labelName = jlabel.getName();
        
        if(lastResult < 0) // cek dahulu apakah lastresult minus / kurang dari 0
            tempt = Math.abs(lastResult); // positifkan hasil lastresult dan simpan ke variable sementara
            
        if((tempt % 1) > 0){ // jika lastresult yang di positifkan lebih besar dari nol (alias pecahan desimal di belakang komanya bukan nol), maka tampilkan lastresult yang asli apa adanya (tanpa format)
                if(labelName.equals("resultLabel"))
                    resultLabel.setText("= " + lastResult);
                else if(labelName.equals("temptResultLabel"))
                    temptResultLabel.setText("" + lastResult + operator);
                
        } else if((tempt % 1) == 0){ // sebaliknya jika lastresult yang di positifkan sama dengan nol (alias pecahan desimal di belakang komanya adalah nol (hasil default perhitungan tipe data double)),
            String slr = String.format("%.0f", lastResult); // maka format dan tampilkan lastresult yang asli (tanpa menampilkan pecahan desimal nol di belakang koma)

            if(labelName.equals("resultLabel"))
                resultLabel.setText("= " + slr);
            else if(labelName.equals("temptResultLabel"))
                temptResultLabel.setText(slr + operator);
        }
    }
    
    /**
     * Simpan input dan lakukan perhitungan sementara
     * @param operator 
     */
    private void storeInput(char operator){
        if(!done){
            String inputFieldText = inputField.getText();

            if(!inputFieldText.equals("")){
                allInputLabel.setText(allInputLabel.getText() + operator);

                try{
                    if(temptResultLabel.getText().equals("")){ // jika true, tampilkan ke label tanpa perhitunngan terlebih dahulu
                        this.operator = operator;

                        lastResult = Double.parseDouble(inputFieldText);
                        temptResultLabel.setText(inputFieldText + operator);
                    } else{ // sebaliknya, langsung lakukang perhitungan dan tampilkan hasil ke label
                        doCalculation(); // lakukan perhitungan terlebih dahulu
                        this.operator = operator; // baru simpan operator selanjutnya
                        
                        printResult(temptResultLabel);
                    }

                    inputField.setText("");
                }catch(NumberFormatException ex){
                    
                    JOptionPane.showMessageDialog(this,
                            "Illegal Notation",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                    
                    clear();
                    
                }catch(Exception ex){
                    
                    clear();
                }
            } else if(inputFieldText.equals("") && !temptResultLabel.getText().equals("")) // jika true berarti mengganti operator
                changeOperator(operator);
        }
    }
    
    /**
     * Lakukan perhitungan.
     * @throws NumberFormatException 
     */
    private void doCalculation() throws NumberFormatException{
        double bil = Double.parseDouble(inputField.getText());
        
        switch(operator){
            case '+':
                lastResult += bil;
                break;
            case '-':
                lastResult -= bil;
                break;
            case 'x':
                lastResult *= bil;
                break;
            case '/':
                lastResult /= bil;
                break;
        }
    }
    
    /**
     * Tampilkan setiap input ke jTextField dan jLabel.
     * @param num 
     */
    private void printButton(String num){
        if(done)
            clear();
        
        inputField.setText(inputField.getText() + num);
        allInputLabel.setText(allInputLabel.getText() + num);
        
        done = false;
    }
    
    /**
     * Ganti operator.
     * @param op 
     */
    private void changeOperator(char op){
        operator = op;
        
        StringBuilder label1Text = new StringBuilder(allInputLabel.getText());
        StringBuilder label2Text = new StringBuilder(temptResultLabel.getText());
        
        label1Text.setCharAt(label1Text.length() - 1, op); // hapus karakter di string paling belakang (string operator) dan ganti dengan karakter (operator) baru
        label2Text.setCharAt(label2Text.length() - 1, op);
        
        allInputLabel.setText(label1Text.toString());
        temptResultLabel.setText(label2Text.toString());
    }
    
    /**
     * Bersihkan output.
     */
    private void clear(){
        allInputLabel.setText("");
        temptResultLabel.setText("");
        resultLabel.setText("");
        inputField.setText("");
    }
    
    
    
