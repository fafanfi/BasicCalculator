    /**
     * Hapus input jTextField satu persatu.
     * @param evt 
     */
    private void bckspButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bckspButtonActionPerformed
        if(done)
            clear();
        else{
            String inputFieldText = inputField.getText();
            
            if(!inputFieldText.equals("")){ // string dari inputfield harus di bandingkan dengan method equals
                StringBuilder textTextField = new StringBuilder(inputFieldText);
                StringBuilder textLabel1 = new StringBuilder(allInputLabel.getText());

                textTextField.deleteCharAt(textTextField.length() - 1); // hapus satu karakter paling belakang
                textLabel1.deleteCharAt(textLabel1.length() - 1); // hapus satu karakter paling belakang

                inputField.setText(textTextField.toString());
                allInputLabel.setText(textLabel1.toString());
            }
        }
    }//GEN-LAST:event_bckspButtonActionPerformed

    /**
     * Hapus input jTextField keseluruhan.
     * dan hapus string label sebanyak input jTextField yang dihapus
     * @param evt 
     */
    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if(done)
            clear();
        else{
            String inputFieldText = inputField.getText();
            int jTextFieldLength = inputFieldText.length();
            
            if(!inputFieldText.equals("")){ // string dari inputfield harus di bandingkan dengan method equals
                inputField.setText("");
                
                StringBuilder label1Text = new StringBuilder(allInputLabel.getText());
                int textLabelLength = label1Text.length();
                
                label1Text.delete((textLabelLength - jTextFieldLength), textLabelLength); // hapus string label sebanyak input jTextField yang dihapus
                allInputLabel.setText(label1Text.toString());
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

